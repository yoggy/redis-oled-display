void LCD_WrDat(unsigned char dat)
{
	unsigned char i = 8;

	digitalWrite(10, 0);
	digitalWrite(5, 1);
	digitalWrite(14, 0);

	while(i--)
	{
		if(dat & 0x80) {
			digitalWrite(12, 1);
		}
		else {
			digitalWrite(12, 0);
		}
		digitalWrite(14, 1);
		digitalWrite(14, 0);
		dat <<= 1;
	}
	digitalWrite(10, 1);
}

void LCD_WrCmd(unsigned char cmd)
{
	unsigned char i = 8;

	digitalWrite(10, 0);
	digitalWrite(5, 0);
	digitalWrite(14, 0);

	while(i--)
	{
		if(cmd & 0x80){
			digitalWrite(12, 1);
		}
		else{
			digitalWrite(12, 0);
		}
		digitalWrite(14, 1);
		digitalWrite(14, 0);
		cmd <<= 1;
	}
	digitalWrite(10, 1);
}

void LCD_Set_Pos(unsigned char x, unsigned char y)
{
	LCD_WrCmd(0xb0+y);
	LCD_WrCmd(((x&0xf0)>>4)|0x10);
	LCD_WrCmd((x&0x0f)|0x00);
}

void LCD_Fill(unsigned char bmp_dat)
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++) {
			LCD_WrDat(bmp_dat);
		}
	}
}

void LCD_CLS(void)
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++) {
			LCD_WrDat(0);
		}
	}
}

